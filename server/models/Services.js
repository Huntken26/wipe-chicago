const mongoose = require('mongoose');

const servicesSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  price: {
    type: Number,
    required: true
  },
  image: {
    type: String,
    required: true
  }
});

const Services = mongoose.model('Service', servicesSchema);

module.exports = Services;
